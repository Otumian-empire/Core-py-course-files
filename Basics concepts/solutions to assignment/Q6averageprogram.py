# 6. write a python program to find the average of 62, 213.5, 67 and 97.7

# input: 62, 213.5, 67 and 97.7 .... there are 4 numbers here
# output : average
# average = sumofallnumbers / number of numbers

sumofallnumbers = 62 + 213.5 + 67 + 97.7
print("The sum of all the 4 numbers is ", sumofallnumbers)

average = sumofallnumbers / 4
print("The average of the 4 numbers, 62, 213.5, 67 and 97.7 is %.2f" %average)