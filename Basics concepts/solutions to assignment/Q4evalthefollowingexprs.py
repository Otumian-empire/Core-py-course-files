# SOLUTION TO QUESTION 4 OF BASIC CONCEPTS ASSIGNMENT
# 4. Write a program to evaluate the following expressions.
# a. 25 / 3 
# b. 20 - 12 / 4 * 2 
# c. 32 % 7 
# d. 3 - 5 % 7
# e. 17 + (5 % 2) - 3 
# f. 15.0 + 3.0 * (2.0 / 5.0)

print("25 / 3 evaluates to ", 25 / 3)
print("20 - 12 / 4 * 2  evaluates to ", 20 - 12 / 4 * 2 )
print("32 % 7  evaluates to ", 32 % 7 )
print("3 - 5 % 7 evaluates to ", 3 - 5 % 7)
print("17 + (5 % 2) - 3  evaluates to ", 17 + (5 % 2) - 3 )
print("15.0 + 3.0 * (2.0 / 5.0) evaluates to ", 15.0 + 3.0 * (2.0 / 5.0))